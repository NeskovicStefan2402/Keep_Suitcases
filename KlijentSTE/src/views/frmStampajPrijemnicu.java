    private void jBtnStampajPrijemnicuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnStampajPrijemnicuActionPerformed
        if(Controller.getInstance().preuzetaPrijemenica(prijemnica)){
            Racun racun=new Racun();
            racun.setCena(new BigDecimal(jLblCena.getText().toString()));
            racun.setDatum(new Date());
            racun.setIdRacuna(new Long(0));
            racun.setKlijent(prijemnica.getKlijent());
            racun.setPrtljag(prijemnica.getPrtljag());
            racun.setRadnik(prijemnica.getRadnik());
            Controller.getInstance().kreirajRacun(racun);
            JOptionPane.showMessageDialog(this,"Uspesno ste kreirali racun!");
            
        }
        
    }//GEN-LAST:event_jBtnStampajPrijemnicuActionPerformed


