    public void uPozadini(){
        jLblIme.setText(prijemnica.getKlijent().getIme());
        jLblPrezime.setText(prijemnica.getKlijent().getPrezime());
        jLblJMBG.setText(prijemnica.getKlijent().getJMBG());
        jLblLomljivost.setText(prijemnica.getPrtljag().isLomljivo()+"");
        jLblTezina.setText(prijemnica.getPrtljag().getTezina()+" Kg");
        jLblDatum.setText(prijemnica.getDatum()+"");
        jLblDatumDanas.setText(new Date()+"");
        jLblNaziv.setText(prijemnica.getPrtljag().getNaziv()+"");
        jLblUsername.setText(prijemnica.getRadnik().getKorisnickoIme());
        long period=new Date().getTime()-prijemnica.getDatum().getTime();
        double cena=(period/1000) *100;
        jLblCena.setText(cena+"");
    }
